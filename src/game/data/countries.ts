/** Build a full CountryMeta from id, falling back to defaults derived from area. */
export function lookupCountry(id: string, name: string, area: number): CountryMeta {
  const entry = META[id];
  if (entry) return { id, ...entry };
  // Heuristic defaults for less-known territories.
  const pop = Math.max(0.4, Math.round(area * 1.4 + 1));
  const value = Math.max(6, Math.round(Math.sqrt(area) * 4 + pop / 6));
  return {
    id,
    iso3: '???',
    name,
    population: pop,
    baseValue: value,
    continent: 'Misc',
    flag: '🏳️',
  };
}

export function getCuratedName(id: string): string | null {
  return META[id]?.name ?? null;
}
